// gateway/index.js
import express from "express";
import { createProxyMiddleware } from "http-proxy-middleware";

const app = express();
const PORT = 8080;

// âœ… Default route (like your nginx / )
app.get("/", (req, res) => {
  res.type("text/plain").send("Express Gateway is running and active!");
});

// âœ… Auth Service routes
app.use(
  "/register",
  createProxyMiddleware({
    target: "http://127.0.0.1:8001",
    changeOrigin: true,
    pathRewrite: { "^/register": "/api/auth/register" },
  })
);

app.use(
  "/login",
  createProxyMiddleware({
    target: "http://127.0.0.1:8001",
    changeOrigin: true,
    pathRewrite: { "^/login": "/api/auth/login" },
  })
);

// âœ… Data Service route
app.use(
  "/getData",
  createProxyMiddleware({
    target: "http://127.0.0.1:8002",
    changeOrigin: true,
    pathRewrite: { "^/getData": "/api/users/getData" },
  })
);

app.listen(PORT, () => {
  console.log(`ðŸš€ Express Gateway running at http://127.0.0.1:${PORT}`);
});
